package JavaBasics;

public class Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(10%3); //остаток від ділення
        System.out.println(a+=b); //зробити + дію і перезаписати змінну
        System.out.println(b-=a); //зробити - дію і перезаписати змінну

        int x = 2;
        int y = 5;
        System.out.println(x*=y); //зробити * дію і перезаписати змінну x
        System.out.println(x);

        int t = 22;
        int g = 2;
        System.out.println(t/=g); //зробити / дію і перезаписати змінну x
        System.out.println(t);


        int z = 4;
        int e = 3;
        System.out.println(z%=e); //знайти остаток від ділення і записати в z
        System.out.println(z);

        z++;
        System.out.println(z); //інкремент збільшити або відняти змінну
        z--;
        System.out.println(z);







    }




}
